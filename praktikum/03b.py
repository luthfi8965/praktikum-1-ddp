# operasi pertama: 6 - 4 ** 2
hasil = 6 - 4 ** 2
print(hasil)

# operasi kedua: 6 * 10 % 4
hasil = 6 * 10 % 4
print(hasil)

# operasi ketiga: 6 * (10 % 4)
hasil = 6 * (10 % 4)
print(hasil)

# operasi keempat: 100 / 30
hasil = 100 / 30
print(hasil)

# operasi kelima: 100 // 30
hasil = 100 // 30
print(hasil)

# operasi keenam: 25.5 // 4
hasil = 25.5 // 4
print(hasil)

# operasi ketujuh: 25 / 10 + 2.5
hasil = 25 / 10 + 2.5
print(hasil)
